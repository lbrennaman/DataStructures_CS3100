#include <vector>
#include <utility>

using namespace std;

/* THIS IS THE VERTEX CLASS
 * 
 * This is the code that was created in lecture by Dr. Cheatham. 
 * This class creates a single vertex of a graph and contains methods for their manipulation
 * This documentation was added by Logan Brennaman on 11/8/21
 *
 */

class Vertex
{
	private:
	
		// The label of this vertex/ the information stored in this node
		char label;
		
		// THIS VECTOR STORES ALL NODES THAT THIS NODE POINTS TO ALONG WITH THEIR WEIGHT
		vector<pair<Vertex*, int> > neighbors;
		
		// Marks a node. Is it marked? Mark = 1. It's not marked? Mark = 0.
		bool mark;

	public:
	
		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													VERTEX CONSTRUCTOR
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														char: label
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *						Constructor to initialize a new NODE with the given label. Is not marked INITIALLY
		 *
		 *________________________________________________________________________________________________________________________________*/

		Vertex(char label)
		{
			this->label = label;
			mark = false;
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													  GETLABEL METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														   VOID
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *											Returns the label stored in this NODE
		 *
		 *________________________________________________________________________________________________________________________________*/

		char getLabel()
		{
			return label;
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													ADD_NEIGHBOR METHOD 1
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *												Vertex pointer: vertex_to_connect
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *				Given a pointer to a vertex, create an edge between them with a default weight of 1 (stored as a pair)
		 *
		 *________________________________________________________________________________________________________________________________*/

		void addNeighbor(Vertex* v)
		{
			neighbors.push_back(make_pair(v, 1));
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													ADD_NEIGHBOR METHOD 2
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *											Vertex pointer: vertex_to_connect
		 *												Integer: weight_of_new_edge
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *		Given a pointer to a vertex, create an edge between them with the second parameter used as its weight (stored as a pair)
		 *
		 *________________________________________________________________________________________________________________________________*/

		void addNeighbor(Vertex* v, int edge_weight)
		{
			neighbors.push_back(make_pair(v, edge_weight));
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *												 GET_NUM_NEIGHBORS METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														   VOID
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *								Returns the number of edges between this node and other nodes
		 *
		 *________________________________________________________________________________________________________________________________*/

		int getNumNeighbors()
		{
			return neighbors.size();
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													GET_NEIGHBOR METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *											Integer: index_of_desired_neighbor
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *								Return the desired neighbor based off of the given index
		 *
		 *________________________________________________________________________________________________________________________________*/

		Vertex* getNeighbor(int i)
		{
			return neighbors[i].first; // The .first here returns the first thing in the pair
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *												GET_NEIGHBOR_WEIGHT METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *											Integer: index_of_desired_neighbor
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *						Return the weight of the edge associated with the index of the desired neighbor
		 *
		 *________________________________________________________________________________________________________________________________*/

		int getNeighborWeight(int i)
		{
			return neighbors[i].second;
		}
	
		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													  IS_MARKED METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														   VOID
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *											Returns the mark field of this NODE
		 *
		 *________________________________________________________________________________________________________________________________*/
		
		bool isMarked()
		{
			return mark;
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													  SET_MARK METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														   VOID
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *											Sets the mark of this NODE to TRUE
		 *
		 *________________________________________________________________________________________________________________________________*/

		void setMark()
		{
			mark = true;
		}

		/*_________________________________________________________________________________________________________________________________//
		 *
		 *													 CLEAR_MARK METHOD
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *														PARAMETERS: 
		 *														   VOID
		 *
		 * _______________________________________________________________________________________________________________________________
		 *
		 *													   FUNCTIONALITY:
		 *											Sets the mark of this NODE to FALSE
		 *
		 *________________________________________________________________________________________________________________________________*/

		void clearMark()
		{
			mark = false;
		}
};
